<?php
  
  //sum of two numbers
  function sum($a, $b) {
    return $a + $b;
  }

  //difference of two numbers
  function difference($a, $b) {
    return $a - $b;
  }

  //show the result of sum and difference
  echo "Sum: " . sum(5, 10) . "<br>";

  echo "Difference: " . difference(5, 10) . "<br>";
?>